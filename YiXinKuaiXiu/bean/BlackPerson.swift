import Foundation

class BlackPerson {
    var name: String?
    var telephone: String?
    var desc: String?
    var date: String?
    
    init(name: String, telephone: String, desc: String, date: String) {
        self.name = name
        self.telephone = telephone
        self.desc = desc
        self.date = date
    }
}